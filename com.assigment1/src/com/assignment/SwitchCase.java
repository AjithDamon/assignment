package com.assignment;

public class SwitchCase {

	public static void main(String[] args) {
		
		
		int n=6;
		switch(n) {
		
		
		case 5:
			System.out.println("Young");
			break;
		case 6:
			System.out.println("Old");
			break;
		default:
			System.out.println("wrong value");
		
		
		
		
		
		
		
		
		
		
		
		
		}
	}

}
